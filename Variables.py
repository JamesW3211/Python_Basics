"""
You can change the value of a variable in your program at any time,and Python will always keep track of its current value
"""

message = "Hello World" #The value hello world was assigned to the variable message.
print(message)          #The value assigned to the variable message was then displayed on screen.

message = "3e3"  #The same variable message can be assigned a new value. The last value assigned to the variable is the one the variable contains.
print(message)   #The new value is displayed on screen.



